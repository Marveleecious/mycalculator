function one(){

    let output = document.getElementById('output');

    let outputValue =  output.value
    if( outputValue  !='Off' && outputValue != "Turn On the calculator"){

   if(outputValue == 0){
    output.value = "";
   }
   output.value +=1
    // console.log(output)
}
else{
    output.value ="Turn On the calculator";
}
}

